// operadores aritmeticos
console.log(1+2) // suma
console.log(2-1) // resta
console.log(3*4) // multiplicacion
console.log(10/2) // division
console.log(10%3) // modulo o residuo
console.log(2**3, "\n") // potencia

// operadores de comparacion
console.log(1 == "1") // igualdad
console.log(1 != 1) // desigualdad
console.log(3 > 2) // mayor que
console.log(3 < 2) // menor que
console.log(3 >= 3) // mayor o igual que
console.log(3 <= 2) // menor o igual que
console.log(3 === "3") // igualdad estricta considerando tipo de dato
console.log(3 !== 3, "\n") // desigualdad estricta

// operadores logicos
console.log(true && true) // and
console.log(true || false) // or
console.log(!false, "\n") // not